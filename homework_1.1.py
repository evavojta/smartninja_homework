#moodchecker

mood = "ladida"

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath three times!")
elif mood == "sad":
    print("Feel hugged, my dear!")
elif mood == "relaxed":
    print("Mmmmmmmmh.")
elif mood == "excited":
    print("I can see you leaping into the air!")
else:
    print("I don't recognize this mood.")
